    var fechaActual = new Date();

    // Definir la fecha de la boda
    var fechaBoda = new Date('12/23/2023');

    // Calcular la diferencia en milisegundos
    var diferenciaEnTiempo = fechaBoda.getTime() - fechaActual.getTime();

    // Calcular la diferencia en días
    var diferenciaEnDias = Math.ceil(diferenciaEnTiempo / (1000 * 3600 * 24));

    // Sumar la diferencia de días a la fecha actual
    var d = new Date(fechaActual.getTime() + diferenciaEnDias * (24 * 60 * 60 * 1000));

    // default example
    simplyCountdown('.simply-countdown-one', {
        year: d.getFullYear(),
        month: d.getMonth() + 1,
        day: d.getDate()
    });

